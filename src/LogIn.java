//For Teachers
public class LogIn {
	private String ini;
	private String password;
	
	public LogIn(String ini, String password){
		this.ini=ini;
		this.password=password;
	}

	public String getIni() {
		return ini;
	}

	public void setIni(String ini) {
		this.ini = ini;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
	

}
